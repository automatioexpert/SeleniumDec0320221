package com.qa.pojos;

/**
 * POJO for QueryVariable
 * @author naveenautomationlabs
 *
 */
public class QueryVariable {

	private int limit;
	private String name;

	public int getLimit() {
		return limit;
	}

	public void setLimit(int limit) {
		this.limit = limit;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
