public class MaxNumbers {

	public static void main(String[] args) {

		int x = 500;
		int y = 400;
		int z = 100;
		
		if(x>y && x>z){//false && true
			System.out.println("x is the highest");
		}
		else if(y>z){//true
			System.out.println("y is the highest");
		}
		else{
			System.out.println("z is the highest");
		}
		
		
		
		
	}

}
